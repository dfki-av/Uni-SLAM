import argparse
import warnings
warnings.filterwarnings("ignore", message="tinycudann was built for lower compute capability")

from src import config
from src.UNISLAM import UNISLAM

def main():
    parser = argparse.ArgumentParser(
        description='Arguments for running UNISLAM.'
    )
    parser.add_argument('config', type=str, help='Path to config file.')

    parser.add_argument('--input_folder', type=str,
                        help='input folder, this have higher priority, can overwrite the one in config file')
    parser.add_argument('--output', type=str,
                        help='output folder, this have higher priority, can overwrite the one in config file')
    args = parser.parse_args()

    cfg = config.load_config(args.config, 'configs/UNISLAM.yaml')
    
    unislam = UNISLAM(cfg, args)

    unislam.run()

if __name__ == '__main__':

    main()